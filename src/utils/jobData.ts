export const getJobStats = () => {
  const companies = [...new Set(mockJobs.map(job => job.company))];
  const departments = [...new Set(mockJobs.map(job => job.department))];
  const positions = [...new Set(mockJobs.map(job => job.title))];
  const totalEmployees = mockJobs.reduce((sum, job) => sum + job.employees, 0);
  
  const companyStats = companies.map(company => {
    const companyJobs = mockJobs.filter(job => job.company === company);
    const totalEmployees = companyJobs.reduce((sum, job) => sum + job.employees, 0);
    return {
      name: company,
      count: totalEmployees,
      growth: Math.floor(Math.random() * 20) + 5 + "%"
    };
  }).sort((a, b) => b.count - a.count);

  const departmentStats = departments.map(department => {
    const deptJobs = mockJobs.filter(job => job.department === department);
    const totalEmployees = deptJobs.reduce((sum, job) => sum + job.employees, 0);
    const percentage = Math.round((totalEmployees / totalEmployees) * 100);
    return {
      name: department,
      count: totalEmployees,
      percentage: Math.min(percentage, 100)
    };
  }).sort((a, b) => b.count - a.count);

  return {
    totalEmployees,
    totalCompanies: companies.length,
    totalDepartments: departments.length,
    totalPositions: positions.length,
    companyStats,
    departmentStats
  };
};
